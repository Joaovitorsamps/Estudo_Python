nome = "João Vitor de Lima Sampaio"

print(nome[0]) #Exibe a primeira letra

print(nome[-1]) #Captura a última letra

print(nome[:9]) #Exibe tudo até 9° letra

print(nome[10:]) #Exibe tudo a partie da 10° letra

print(nome[0:9] )#Exibe tudo de uma posição definida até a outra

print(nome[:]) #Exibe tudo 

print(nome[::-1]) #Exibe a string de trás para a frente

print(nome[0:3:2]) #Definindo o início e o fim, pode-se também colocar o "passo" que vai definir quantos caracteres ele vai pular até ler o próximo, neste caso ele retorna "Jã"